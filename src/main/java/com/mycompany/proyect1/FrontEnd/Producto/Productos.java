      // M√©todo para cargar los datos del archivo en el modelo de la tabla
    public  void cargarDatosEnTabla(String ruta, DefaultTableModel modelo) {

        System.out.println( "===>  "+ruta);

        try (BufferedReader br = new BufferedReader(new FileReader(ruta))) {
            String linea;
            while ((linea = br.readLine()) != null) {
                // Separar la l√≠nea por el delimitador ";"
                String[] partes = linea.split(";");
                if (partes.length == 3) { // Asegurarse de que haya exactamente 3 elementos
                    String nombre = partes[0].trim();
                    String precio = partes[1].trim();
                    String cantidad = partes[2].trim();

                    // Agregar los datos a la tabla
                    modelo.addRow(new Object[]{nombre, precio, cantidad});
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("Error al leer el archivo.");
        }
    }


